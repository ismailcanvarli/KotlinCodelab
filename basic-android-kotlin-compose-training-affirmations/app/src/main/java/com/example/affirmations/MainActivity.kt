class MainActivity : ComponentActivity() {

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContent {
            AffirmationsTheme {
                // A surface container using the 'background' color from the theme
                Surface(
                    modifier = Modifier.fillMaxSize(),
                    color = MaterialTheme.colorScheme.background
                ) {
                    AffirmationsApp()
                }
            }
        }
    }
}

//Burada uygulamayı oluşturuyor ve affirmationList composable'nı çağırıyor
//ona da gerekli değerleri vermek için oluşturduğu dataSource değerlerini veriyor.
@Composable
fun AffirmationsApp() {
    //App çalıştığı zaman liste dataSource sınıfından çağırılıyor.
    AffirmationList(
        affirmationList = Datasource().loadAffirmations(),
    )
}

@Composable
fun AffirmationList(affirmationList: List<Affirmation>, modifier: Modifier = Modifier) {
    //Scrollable list lazyColumn ile oluşturulur.
    //item sadece lazyColumn'a ait bir öğe. İçindeki eşyaları tanımılıyor.
    LazyColumn(modifier = modifier) {
        //Burada itemler o nesneye gelindiğinde oluşturuluyor.
        //bu sayede optimizasyon sağlanıyor.
        items(affirmationList) { affirmation ->
            AffirmationCard(
                affirmation = affirmation,
                modifier = Modifier.padding(8.dp)
            )
        }
    }
}

@Composable
fun AffirmationCard(affirmation: Affirmation, modifier: Modifier = Modifier) {
    //Card bir layout oluyor. Bu layout bir kart gibi görüntü veriyor.
    Card(modifier = modifier) {
        Column {
            //Burada ımage için maks genişlik veriyor. Sonra yüksekliğini ayarlıyor
            //son olarakta içeriği uygun şekilde ayarlıyor.
            Image(
                painter = painterResource(affirmation.imageResourceId),
                contentDescription = stringResource(affirmation.stringResourceId),
                modifier = Modifier
                    .fillMaxWidth()
                    .height(194.dp),
                contentScale = ContentScale.Crop
            )
            Text(
                text = LocalContext.current.getString(affirmation.stringResourceId),
                modifier = Modifier.padding(16.dp),
                style = MaterialTheme.typography.headlineSmall
            )
        }
    }
}

